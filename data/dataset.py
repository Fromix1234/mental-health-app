    if num_examples is None:
        sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
        from config import CONFIG
        num_examples = CONFIG.data.dataset_size
    if os.path.exists("data/therapy_data.json"):
        with open("data/therapy_data.json", "r", encoding="utf-8") as f:
            data = json.load(f)
        print(f"Loaded {len(data)} examples")
        if len(data) < num_examples:
            print(f"Regenerating: need {num_examples}")
            data = generate_dataset(num_examples)
    else:
        data = generate_dataset(num_examples)

    if os.path.exists("data/tokenizer.json"):
        tokenizer = Tokenizer.from_file("data/tokenizer.json")
        print(f"Loaded tokenizer. Vocab: {tokenizer.get_vocab_size()}")
    else:
        tokenizer = train_tokenizer(data)

    return data, tokenizer


