def generate_dataset(num_examples=2000):
    import random
    data = []
    scenarios = THERAPY_SCENARIOS * (num_examples // len(THERAPY_SCENARIOS) + 1)

    for i in range(num_examples):
        scenario = random.choice(scenarios)

        if random.random() < 0.4:
            user_msg = random.choice(RANDOM_USER_MESSAGES)
        else:
            user_msg = scenario["user"]
            if random.random() < 0.3:
                prefixes = ["Скажи, ", "Подскажи, ", "Объясни, ", ""]
                suffixes = ["...", "", " :(", " помоги"]
                user_msg = random.choice(prefixes) + user_msg + random.choice(suffixes)

        therapist_msg = scenario["therapist"]

        persona = random.choice([
            "Ты — опытный психолог с теплым и поддерживающим стилем.",
            "Ты — психотерапевт. Отвечай заботливо и профессионально.",
            "Ты — специалист по ментальному здоровью. Будь эмпатичен, но честен.",
        ])

        conversation = (
            f"[INST] {persona}\nПользователь: {user_msg} [/INST]\n"
            f"Терапевт: {therapist_msg}"
        )
        data.append({"text": conversation})

    os.makedirs(os.path.dirname("data/"), exist_ok=True)
    with open("data/therapy_data.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Generated {len(data)} examples")
    return data


def train_tokenizer(data, vocab_size=8192):
    tokenizer = Tokenizer(models.BPE())
    tokenizer.pre_tokenizer = pre_tokenizers.ByteLevel(add_prefix_space=False)

    trainer = trainers.BpeTrainer(
        vocab_size=vocab_size,
        special_tokens=["[PAD]", "[UNK]", "[BOS]", "[EOS]", "[INST]", "[/INST]"],
        min_frequency=2,
    )

    texts = [item["text"] for item in data]
    tokenizer.train_from_iterator(texts, trainer)

    tokenizer.save("data/tokenizer.json")
    print(f"Tokenizer trained. Vocab size: {tokenizer.get_vocab_size()}")
    return tokenizer


def load_or_create_dataset(num_examples=None):
    if num_examples is None:
        sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
        from config import CONFIG
        num_examples = CONFIG.data.dataset_size
    if os.path.exists("data/therapy_data.json"):
        with open("data/therapy_data.json", "r", encoding="utf-8") as f:
            data = json.load(f)
        print(f"Loaded {len(data)} examples from file")
        if len(data) < num_examples:
            print(f"Too few examples ({len(data)} < {num_examples}), regenerating...")
            data = generate_dataset(num_examples)
    else:
        data = generate_dataset(num_examples)

    if os.path.exists("data/tokenizer.json"):
        from tokenizers import Tokenizer
        tokenizer = Tokenizer.from_file("data/tokenizer.json")
        print(f"Loaded tokenizer. Vocab: {tokenizer.get_vocab_size()}")
    else:
        tokenizer = train_tokenizer(data)

    return data, tokenizer


